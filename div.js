function showDiv() {
    var div = document.querySelector(".type");
    div.style.opacity = "1"; // Ao passar o mouse sobre a div, ela ficará visível
  }
  
  function hideDiv() {
    var div = document.querySelector(".type");
    div.style.opacity = "0"; // Ao retirar o mouse da div, ela ficará invisível novamente
  }
  